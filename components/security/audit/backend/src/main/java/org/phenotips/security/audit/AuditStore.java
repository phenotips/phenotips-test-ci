package org.phenotips.security.audit;

import org.xwiki.component.annotation.Role;
import org.xwiki.model.reference.DocumentReference;
import org.xwiki.stability.Unstable;
import org.xwiki.users.User;

import java.util.Calendar;
import java.util.List;

import javax.annotation.Nonnull;
import javax.annotation.Nullable;

/**
 * Stores and retrieves {@link AuditEvent audit events}.
 *
 * @version $Id$
 * @since 1.4
 */
@Unstable("New API introduced in 1.4")
@Role
public interface AuditStore
{
    /**
     * Stores an audit event.
     *
     * @param event the event to store
     */
    void store(@Nonnull AuditEvent event);

    /**
     * Retrieves all the events affecting a specific entity.
     *
     * @param entity a reference to the target entity
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEventsForEntity(@Nonnull DocumentReference entity);

    /**
     * Retrieves all the events of a specific type affecting a specific entity.
     *
     * @param entity a reference to the target entity
     * @param type the event type, for example {@code view}, {@code edit}, {@code export}
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEventsForEntity(@Nonnull DocumentReference entity, @Nullable String type);

    /**
     * Retrieves all the events generated by a specific user.
     *
     * @param user the user whose events to retrieve, may be {@code null}
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEventsForUser(@Nullable User user);

    /**
     * Retrieves all the events generated by a specific user, coming from a specific IP.
     *
     * @param user the user whose events to retrieve, may be {@code null}
     * @param ip the ip where the request came from
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEventsForUser(@Nullable User user, @Nullable String ip);

    /**
     * Retrieves all the events of a specific type generated by a specific user, coming from a specific IP.
     *
     * @param user the user whose events to retrieve, may be {@code null}
     * @param ip the ip where the request came from
     * @param type the event type, for example {@code view}, {@code edit}, {@code export}
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEventsForUser(@Nullable User user, @Nullable String ip, @Nullable String type);

    /**
     * Retrieves all the events matching a specific event template. Parameters {@code fromTime} and {@code toTime}
     * define an interval for the time stamp. Events matching all non-null fields from the template exactly will be
     * returned.
     *
     * @param eventTemplate the event template that can have non-{@code null} fields which are to be used as filters
     *            when retrieving matching events. If all fields are null or the template itself is {@code null}, all
     *            events match and should be returned.
     * @param fromTime start of the interval for the time stamp filter. If {@code null}, matching events from the
     *            beginning will be retrieved.
     * @param toTime end of the interval for the time stamp filter. If {@code null}, matching events until the present
     *            moment will be retrieved.
     * @param start starting index of events to return
     * @param maxResults upper bound of events number to return, 0 or negative means no limit
     * @return a list of audited events, may be empty
     */
    @Nonnull
    List<AuditEvent> getEvents(@Nullable AuditEvent eventTemplate, @Nullable Calendar fromTime,
        @Nullable Calendar toTime, int start, int maxResults);

    /**
     * Counts all the events affecting a specific entity.
     *
     * @param entity a reference to the target entity
     * @return an events count, or {@code -1} for internal errors
     */
    long countEventsForEntity(@Nonnull DocumentReference entity);

    /**
     * Counts all the events of a specific type affecting a specific entity.
     *
     * @param entity a reference to the target entity
     * @param type the event type, for example {@code view}, {@code edit}, {@code export}
     * @return an events count, or {@code -1} for internal errors
     */
    long countEventsForEntity(@Nonnull DocumentReference entity, @Nullable String type);

    /**
     * Counts all the events generated by a specific user.
     *
     * @param user the user whose events to count, may be {@code null}
     * @return an events count, or {@code -1} for internal errors
     */
    long countEventsForUser(@Nullable User user);

    /**
     * Counts all the events generated by a specific user, coming from a specific IP.
     *
     * @param user the user whose events to count, may be {@code null}
     * @param ip the ip where the request came from
     * @return an events count, or {@code -1} for internal errors
     */
    long countEventsForUser(@Nullable User user, @Nullable String ip);

    /**
     * Counts all the events of a specific type generated by a specific user, coming from a specific IP.
     *
     * @param user the user whose events to count, may be {@code null}
     * @param ip the ip where the request came from
     * @param type the event type, for example {@code view}, {@code edit}, {@code export}
     * @return an events count, or {@code -1} for internal errors
     */
    long countEventsForUser(@Nullable User user, @Nullable String ip, @Nullable String type);

    /**
     * Counts all the events of a specific event template. Parameters {@code fromTime} and {@code toTime} define an
     * interval for the time stamp. Events matching all non-null fields from the template exactly will be counted.
     *
     * @param eventTemplate the event template that can have non-{@code null} fields which are to be used as filters
     *            when matching events. If all fields are null or the template itself is {@code null}, all events match
     *            and should be counted.
     * @param fromTime start of the interval for the time stamp filter. If parameter fromTime is {@code null}, matching
     *            events from the beginning will be counted.
     * @param toTime end of the interval for the time stamp filter. If parameter toTime is {@code null}, matching events
     *            until the present moment will be counted.
     * @return total number of events satisfying template and time criteria, or {@code -1} for internal errors
     */
    long countEvents(@Nullable AuditEvent eventTemplate, @Nullable Calendar fromTime, @Nullable Calendar toTime);
}
